package Unit01.Topic1_4;

public class Main {
  public static void main(String[] args) {
    int x = 23; //Sets x to 23
    int y = 17; //Sets y to 17
    x *= 4; //Multiplies x by 4, making it 92
    y++; //Adds 1 to y, making it 18
    x /= 3; //Divides x by 3, making it 30
    y -= 9; //Subtracts 9 from y, making it 9
    x %= y; //Modulos 9 from x, making it 3
    System.out.println(x); //Prints x, which is 3
  }
  
}
